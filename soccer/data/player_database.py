class PlayerDatabase:
    """Interface to player database."""
    
    def __init__(self):
        self.players = KNOWN_PLAYERS
    
    def get_player(self, name: str) -> Optional[PlayerStats]:
        """Get player by name (case-insensitive)."""
        return self.players.get(name.lower().strip())
    
    def get_stats_for_edge(self, name: str) -> Optional[Dict]:
        """Get player stats formatted for edge generation."""
        player = self.get_player(name)
        if player:
            return player.to_dict()
        return None
    
    def search_players(self, query: str) -> list:
        """Search for players by partial name match."""
        query = query.lower()
        return [p for name, p in self.players.items() if query in name]
    
    def list_by_team(self, team: str) -> list:
        """List all players from a team."""
        team = team.lower()
        return [p for p in self.players.values() if team in p.team.lower()]
    
    def list_by_league(self, league: str) -> list:
        """List all players from a league."""
        league = league.lower()
        return [p for p in self.players.values() if league in p.league.lower()]
    
    def __len__(self) -> int:
        return len(self.players)


